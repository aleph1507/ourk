    public function update_permissions(Request $request, $id)
    {
        $user = User::find($id);
        $this->validate($request, [
                'selclearance' => 'required'
            ]);
        
        $user->clearance = $request->selclearance;

        $user->save();
        Session::flash('success', 'Пермисиите се сменети.');

        return redirect()->route('admin.users');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        $user->delete();
        Session::flash('success', 'Корисникот е избришан.');
        return redirect()->route('admin.users');
    }
